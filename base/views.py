def art_frase(frase):
    linhas = [""] * 13

    for letra in frase:
        arte_letra = print_art(letra)
        if arte_letra[0] == "Letra inválida!":
            continue  
        for i in range(13):
            linhas[i] += arte_letra[i].rstrip() + " "

    linhas = [linha.strip() for linha in linhas if linha.strip()]
        
    return linhas 

def gerar_arte(request):
    arte = []
    if request.method == 'GET':
        frase = request.GET.get('frase')
        if frase:
            arte = art_frase(frase)
    
    return render(request, 'Base/art.html', {'arte': arte})
